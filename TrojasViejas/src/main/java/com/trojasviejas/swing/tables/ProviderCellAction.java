package com.trojasviejas.swing.tables;

import com.trojasviejas.models.utility.ProviderActionModel;
import java.awt.Color;
import java.awt.Component;
import javax.swing.DefaultCellEditor;
import javax.swing.JCheckBox;
import javax.swing.JTable;

public class ProviderCellAction extends DefaultCellEditor{
    private ProviderActionModel data;

    public ProviderCellAction() {
        super(new JCheckBox());
    }

    @Override
    public Component getTableCellEditorComponent(JTable jtable, Object o, boolean bln, int i, int i1) {
        data = (ProviderActionModel) o;
        Action cell = new Action(data);
        cell.setBackground(new Color(239, 244, 255));
        return cell;
    }

    //  This method to pass data to cell render when focus lose in cell
    @Override
    public Object getCellEditorValue() {
        return data;
    }
}
